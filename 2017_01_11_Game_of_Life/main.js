  function emptyData() {
    var arr = [];
    for (var row = 1; row <= $size; row++) {
      arr.push([]);
      for (var col = 1; col <= $size; col++) {
        arr[row - 1].push(0);
      }
    }
    return arr;
  }

  function initContainer($main) {
    $gapSize = 2;
    $cellSize = ($main.width() - ($size + 1) * $gapSize) / $size;

    for (var row = 1; row <= $size; row++) {
      for (var col = 1; col <= $size; col++) {
        var cellElement = document.createElement('div');
        var status = $mainData[row - 1][col - 1];
        cellElement.setAttribute('id', 'r' + row + 'c' + col);
        cellElement.style['position'] = 'absolute';
        cellElement.style['background-color'] = status == 0 ? $emptyColor: $aliveColor;
        cellElement.style['width'] = $cellSize + 'px';
        cellElement.style['height'] = $cellSize + 'px';
        cellElement.style['top'] = ($gapSize * row + (row - 1) * $cellSize) + 'px';
        cellElement.style['left'] = ($gapSize * col + (col - 1) * $cellSize) + 'px';
        $main.append(cellElement);
      }
    }
  }

  function nextGenerationResult() {
    var result = [];
    for (var row = 1; row <= $size; row++) {
      result.push([]);
      var rowIndex = row - 1;
      for (var col = 1; col <= $mainData[rowIndex].length; col++) {
        var cellAlive = 0;
        for (var position of getSurroundingPositions(row, col, $size)) {
          cellAlive += $mainData[position.row - 1][position.col - 1];
        }
        // console.log('row : '+ row + ' col : ' + col);
        // console.log('Current State : ' + $mainData[rowIndex][col - 1]);
        // console.log('CELL ALIVE : ' + cellAlive);
        if ($mainData[rowIndex][col - 1] === 0) {
          /* The Cell is Empty and Neighbor Cells Count == 3 */
          if (cellAlive === 3) {
            result[rowIndex].push(1);
          } else result[rowIndex].push(0);
        } else {
          /* The Cell Exist */
          if (cellAlive <= 1  || cellAlive >= 4) {
            result[rowIndex].push(0);
          } else result[rowIndex].push(1);
        }
      }
    }
    return result;
  }

